import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const tokenBase =
	'./node_modules/@designsystem-se/af/dist/digi-arbetsformedlingen/styles/src/tokens-documentation';

const af25Path = resolve(`${tokenBase}/web-AF25.json`);
const webPath = resolve(`${tokenBase}/web.json`);

const af25 = JSON.parse(readFileSync(af25Path, 'utf-8'));
const web = JSON.parse(readFileSync(webPath, 'utf-8'));

// web-AF25.json → semantic color tokens
// Key "text--primary" → "--color-text-primary: var(--digi--color--text--primary)"
const colorLines = Object.entries(af25).map(([key, entry]) => {
	const twName = '--color-' + key.replace(/--/g, '-');
	return `\t${twName}: var(${entry.digiCSSVariable});`;
});

// web.json → primitive tokens filtered by category
const byType = (type, item) =>
	web.filter(
		(t) =>
			t.attributes?.category === 'global' &&
			t.attributes?.type === type &&
			(item === undefined || t.attributes?.item === item)
	);

const spacingLines = byType('spacing').map(
	(t) => `\t--spacing-${t.attributes.item}: var(--${t.name});`
);

const radiusLines = byType('border-radius').map(
	(t) => `\t--radius-${t.attributes.item}: var(--${t.name});`
);

// font-size tokens have their size name in "subitem"
const textLines = byType('typography', 'font-size').map(
	(t) => `\t--text-${t.attributes.subitem}: var(--${t.name});`
);

const sections = [
	['Colors — semantic tokens from the digi alias layer (AF25)', colorLines],
	['Spacing', spacingLines],
	['Border radius', radiusLines],
	['Font sizes', textLines]
];

const body = sections
	.map(([label, lines]) => `\t/* ${label} */\n${lines.join('\n')}`)
	.join('\n\n');

const output = `/* Auto-generated by scripts/generate-tailwind-theme.js — do not edit manually. */
/* Run \`node scripts/generate-tailwind-theme.js\` to regenerate after updating digi-styles tokens. */

@theme {
${body}
}
`;

const outPath = './src/lib/styles/tailwind-digi-theme.css';
writeFileSync(outPath, output);

console.log(`Generated ${outPath}`);
console.log(`  ${colorLines.length} color tokens`);
console.log(`  ${spacingLines.length} spacing tokens`);
console.log(`  ${radiusLines.length} radius tokens`);
console.log(`  ${textLines.length} text-size tokens`);
